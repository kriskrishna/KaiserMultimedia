package org.kp.wpp.portlet.search.beans;

import java.io.Serializable;

public class Facet implements Serializable{

	private String title;
	private int count; 
	private String selectHref;
	private String deSelectHref;
	private boolean selected;
	// This property is set only for simplifying WI reporting tags
	// No business significance.
	private String otherSelectedBins;
	
	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}
	/**
	 * @param title the title to set
	 */
	public void setTitle(final String title) {
		this.title = title;
	}
	/**
	 * @return the count
	 */
	public int getCount() {
		return count;
	}
	/**
	 * @param count the count to set
	 */
	public void setCount(final int count) {
		this.count = count;
	}
	/**
	 * @return the selectHref
	 */
	public String getSelectHref() {
		return selectHref;
	}
	/**
	 * @param selectHref the selectHref to set
	 */
	public void setSelectHref(final String href) {
		this.selectHref = href;
	}
	/**
	 * @return the selected
	 */
	public boolean isSelected() {
		return selected;
	}
	/**
	 * @param selected the selected to set
	 */
	public void setSelected(final boolean selected) {
		this.selected = selected;
	}
	/**
	 * @return the deSelectHref
	 */
	public String getDeSelectHref() {
		return deSelectHref;
	}
	/**
	 * @param deSelectHref the deSelectHref to set
	 */
	public void setDeSelectHref(final String deSelectHref) {
		this.deSelectHref = deSelectHref;
	}
	/**
	 * @return the otherSelectedBins
	 */
	public String getOtherSelectedBins() {
		return otherSelectedBins;
	}
	/**
	 * This property is set only for simplifying WI reporting tags, No business significance.
	 * @param otherSelectedBins the otherSelectedBins to set
	 */
	public void setOtherSelectedBins(final String otherSelectedBins) {
		this.otherSelectedBins = otherSelectedBins;
	}

	private static final long serialVersionUID = 1L;
}
